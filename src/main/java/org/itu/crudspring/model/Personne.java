package org.itu.crudspring.model;

import jakarta.persistence.*;

@Entity
@Table(name = "personne")
public class Personne {

    @Id
    private String id;

    private String nom;
    private String prenom;
    private int age;

    // Getters et Setters
    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getPrenom() {
        return prenom;
    }

    public void setPrenom(String prenom) {
        this.prenom = prenom;
    }
}
